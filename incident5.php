        <p>On February 10, 1983, an Iranian oil platform above the Nowruz oil field in the northern Persian Gulf was struck by a tanker. The impact caused the platform to list 45 degrees, and corrosion and wave energy worked to topple the platform and rupture the platform’s wellhead. The well leaked about 1,500 barrels (63,000 gallons) of oil into the Persian Gulf per day before it was finally capped in September 1983. During the early 1980s the northern Persian Gulf was a contested war zone as part of the Iran-Iraq War, and a different nearby platform was attacked by Iraqi helicopters only one month after the tanker collision. The damage to this second platform spilled some 733,000 barrels (about 31 million gallons) of oil into the gulf before it was capped more than two years later. Iran’s capping and repair operations were performed under fire from the Iraqis, and some 20 people died trying to cap the wells. Estimates suggest that roughly 80 million gallons of oil were spilled as a result of these two incidents. Skimmers and other equipment were able to clean up some of the oil, but an estimated two-thirds of the total amount fell to the seafloor as tar balls after sand mixed with floating oil at the surface.</p>
      

